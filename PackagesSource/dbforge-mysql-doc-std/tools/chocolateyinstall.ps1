$url        = 'https://www.devart.com/dbforge/mysql/documenter/documentermysql12std.exe' 
$url64      = '' 
$packageArgs = @{
  packageName   = $env:ChocolateyPackageName
  unzipLocation = $toolsDir
  fileType      = 'exe' 
  url           = $url
  url64bit      = $url64
  softwareName  = 'dbForge Documenter for MySQL Server, v1.2.20 Standard Edition' 
  checksum      = '313B6FB26E9EF0D1749C78EC1663641351D996DEB46A1A0E270883980563B282'
  checksumType  = 'sha256' 
  silentArgs    = '/VERYSILENT'
  validExitCodes= @(0, 3010, 1641)
}
Install-ChocolateyPackage @packageArgs 