$url        = 'https://www.devart.com/dbforge/sql/studio/dbforgesql58std.exe' 
$packageArgs = @{
  packageName   = $env:ChocolateyPackageName
  unzipLocation = $toolsDir
  fileType      = 'exe' 
  url           = $url 
  softwareName  = 'dbForge Studio for SQL Server, v5.8 Standard Edition' 
  checksum      = 'BFCA26DEFE51BE0F237BB1DDA009A6E4AA6746B1A3C15F05FFA1B235F747A8F6'
  checksumType  = 'sha256' 
  silentArgs    = '/VERYSILENT'
  validExitCodes= @(0, 3010, 1641)
}
Install-ChocolateyPackage @packageArgs 