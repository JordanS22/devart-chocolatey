  packageName   = $env:ChocolateyPackageName
  unzipLocation = $toolsDir
  fileType      = 'exe' 
  url           = $url 
  softwareName  = 'Devart dbForge Data Generator for Oracle, v2.0.63 Trial Edition' 
  checksum      = '3BA77FFEAD9A9926345384E57F7E1FAD37956AEB4350A5842B477DD070E8A7F2'
  checksumType  = 'sha256' 
  silentArgs    = '/VERYSILENT'
  validExitCodes= @(0, 3010, 1641)
}
Install-ChocolateyPackage @packageArgs 